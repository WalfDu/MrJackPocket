public class Joueur {
	private Boolean roles;
	private String nom;

	// Constructors
	public Joueur() {
	};

	public void setRoles(Boolean newVar) {
		roles = newVar;
	}

	public Boolean getRoles() {
		return roles;
	}

	public void setNom(String newVar) {
		nom = newVar;
	}

	public String getNom() {
		return nom;
	}
}
